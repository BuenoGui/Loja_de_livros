$(document).ready(function() {
        $('#Carrinho').click(function() {
            $('.carrinho').animate({
                width: 'toggle'
            }, 1300);
            $('.carrinho').css({
                display: 'flex'
            })    
        });
        $('#Fechar').click(function() {
            $('.carrinho').animate({
                width: 'toggle'
            }, 1300);
            $('.carrinho').css({
                display: 'flex'
            }, )
        });



});